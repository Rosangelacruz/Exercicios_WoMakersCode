# 1. Crie duas tuplas. Concatene-as para formar uma nova tupla.

# Criando duas tuplas
tupla1 = (1, 2, 3, 4, 5 )
tupla2 = (6, 7, 8, 9, 10)

# Concatenando as duas tuplas para formar uma nova tupla
tupla_concatenada = tupla1 + tupla2

# Exibindo a nova tupla concatenada
print(f'Tupla 1: {tupla1}')
print(f'Tupla 2: {tupla2}')
print(f'Tupla concatenada: {tupla_concatenada}')